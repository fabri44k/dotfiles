-- Set leader key
vim.g.mapleader = ' '
vim.g.maplocalleader = ' '

-- Disable the spacebar key's default behavior in Normal and Visual modes
vim.keymap.set({ 'n', 'v' }, '<Space>', '<Nop>', { silent = true })

-- For conciseness
local opts = { noremap = true, silent = true }

-- save file
vim.keymap.set('n', '<C-s>', '<cmd> w <CR>', opts)

-- quit file
vim.keymap.set('n', '<C-q>', '<cmd> q <CR>', opts)

-- delete single character without copying into register
vim.keymap.set('n', 'x', '"_x', opts)

-- Vertical scroll and center
vim.keymap.set('n', '<C-d>', '<C-d>zz', opts)
vim.keymap.set('n', '<C-u>', '<C-u>zz', opts)

-- Stay in indent mode
vim.keymap.set('v', '<', '<gv', opts)
vim.keymap.set('v', '>', '>gv', opts)

-- Keep last yanked when pasting
vim.keymap.set('v', 'p', '"_dP', opts)

-- In Insert mode, premere "jj" esce in Normal mode
vim.keymap.set('i', 'jj', '<Esc>', opts)

-- Find and center
vim.keymap.set('n', 'n', 'nzzzv', opts)
vim.keymap.set('n', 'N', 'Nzzzv', opts)

-- Stay in indent mode
vim.keymap.set('v', '<', '<gv', opts)
vim.keymap.set('v', '>', '>gv', opts)

-- Buffers
vim.keymap.set('n', '<Tab>', ':bnext<CR>', opts)
vim.keymap.set('n', '<S-Tab>', ':bprevious<CR>', opts)
vim.keymap.set('n', '<leader>x', ':Bdelete<CR>', opts)    -- close buffer
vim.keymap.set('n', '<leader>b', '<cmd> enew <CR>', opts) -- new buffer
-- BufferLine extension

-- Sposta buffer a sinistra
vim.keymap.set('n', '<leader><S-h>', '<cmd>BufferLineMovePrev<CR>', { desc = 'Buffer ⇐' })
-- Sposta buffer a destra
vim.keymap.set('n', '<leader><S-l>', '<cmd>BufferLineMoveNext<CR>', { desc = 'Buffer ⇒' })

-- CTR + A
vim.keymap.set('n', '<C-a>', 'ggVG', opts)
vim.keymap.set('v', '<C-a>', 'ggVG', opts)

-- Remapping di y$ a Y
vim.keymap.set('n', 'Y', 'y$', opts)

-- Outline
vim.keymap.set('n', '<leader>o', '<cmd>Outline<CR>', opts)

-- Navigazione rapida tra split
vim.keymap.set('n', '<C-h>', '<C-w>h', { silent = true })
vim.keymap.set('n', '<C-l>', '<C-w>l', { silent = true })
vim.keymap.set('n', '<C-k>', '<C-w>k', { silent = true })
vim.keymap.set('n', '<C-j>', '<C-w>j', { silent = true })

-- Diagnostic keymaps
vim.keymap.set('n', '[d', function()
    vim.diagnostic.jump { count = -1, float = true }
end, { desc = 'Go to previous diagnostic message' })

vim.keymap.set('n', ']d', function()
    vim.diagnostic.jump { count = 1, float = true }
end, { desc = 'Go to next diagnostic message' })

-- Vertical Split with terminal in the same folder as the current buffer

vim.api.nvim_create_user_command('Vst', function()
    local current_dir = vim.fn.expand '%:p:h'
    if current_dir ~= '' then
        vim.cmd('lcd ' .. vim.fn.fnameescape(current_dir))
    end
    vim.cmd 'vertical terminal'
end, {})
